#include <glmc.h>

float	vec4f_sqrt(t_vec4f *v)
{
	return (v->x * v->x + v->y * v->y + v->z * v->z + v->w * v->w);
}

float	vec4f_dot(t_vec4f *s, t_vec4f *d)
{
	return (s->x * d->x + s->y * d->y + s->z * d->z + s->w * d->w);
}
