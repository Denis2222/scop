#include "libft.h"

int		ft_tolower(int c)
{
	if (ft_isalpha(c) && c > 64 && c < 91)
	{
		return (c + 32);
	}
	return (c);
}

char	*ft_strtolower(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		str[i] = ft_tolower(str[i]);
		i++;
	}
	return (str);
}
